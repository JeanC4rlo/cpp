#include <iostream>

using namespace std;

int main() {
    // Sem quebra
    cout << "Eu farmo muita aura, ";
    cout << "sou aura mais ego";

    cout << endl << endl;

    // Com quebra (\n)
    cout << "Eu farmo muita aura\n";
    cout << "sou aura mais ego";
    
    cout << endl << endl;

    // Com quebra (endl)
    cout << "Eu farmo muita aura" << endl;
    cout << "sou aura mais ego";

    cout << endl;
    return 0;
}
